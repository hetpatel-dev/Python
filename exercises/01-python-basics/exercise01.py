# Exercise 1: About Me

# Create three variables: city, country, population. Assign values and print a sentence like: "I live in [city], [country]. Population: [population].".

city = input("Which City do you live in? : ")
country = input("Which country do you live in? : ")
population = input("What is the population of your country? : ")

print(f"I live in {city}, {country}. Population: {population}.")

# Output:
# Which City do you live in? : Ahmedabad
# Which country do you live in? : India
# What is the population of your country? : 1.45 crore
# I live in Ahmedabad, India. Population: 1.45 crore.
