# Exercise 3: Type Explorer
# Create a variable of each type (int, float, str, bool). Print each value and its type using type().

# Output should look like:

# 42 is <class 'int'>
# 3.14 is <class 'float'>
# hello is <class 'str'>
# True is <class 'bool'>


int_var = 59
float_var = 289.2390
str_var = "Cat"
bool_var = False

print(f"{int_var} is {type(int_var)}")
print(f"{float_var} is {type(float_var)}")
print(f"{str_var} is {type(str_var)}")
print(f"{bool_var} is {type(bool_var)}")

# Output:
# 59 is <class 'int'>
# 289.239 is <class 'float'>
# Cat is <class 'str'>
# False is <class 'bool'>
